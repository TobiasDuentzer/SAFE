package service;

public class SolutionBoolean {
    private final int[] first;
    private final boolean second;

    public SolutionBoolean(int[] first, boolean second) {
        this.first = first;
        this.second = second;
    }

    public int[] getFirst() {
        return first;
    }

    public boolean getSecond() {
        return second;
    }
}
